#include <stdio.h>

int main() {
    // กำหนดค่าตัวแปรเริ่มต้น
    int x = 12;
    int y = 7;
    int z = 12;

    printf("กำหนดให้: x = %d, y = %d, z = %d\n\n", x, y, z);

    // 1. x > y
    printf("1. x > y : %d\n", x > y); // true เพราะ 12 > 7

    // 2. x < z
    printf("2. x < z : %d\n", x < z); // false เพราะ 12 ไม่น้อยกว่า 12

    // 3. x == z
    printf("3. x == z : %d\n", x == z); // true เพราะ 12 เท่ากับ 12

    // 4. x != y
    printf("4. x != y : %d\n", x != y); // true เพราะ 12 ไม่เท่ากับ 7

    // 5. !(2+5 >= y) || (5 != (5/3))
    printf("5. !(2+5 >= y) || (5 != (5/3)) : %d\n", !(2 + 5 >= y) || (5 != (5 / 3))); // true เพราะ !(7 >= 7) เป็น false แต่ (5 != 1) เป็น true

    // 6. !(x < y)
    printf("6. !(x < y) : %d\n", !(x < y)); // true เพราะ !(12 < 7) เป็น true

    // 7. (x+y) > (z * 2)
    printf("7. (x+y) > (z * 2) : %d\n", (x + y) > (z * 2)); // true เพราะ (12 + 7) > (12 * 2) เป็น true (19 > 24 เป็น false)
 
    // 8. (x % 2 == 0) || (y % 2 == 1)
    printf("8. (x %% 2 == 0) || (y %% 2 == 1) : %d\n", (x % 2 == 0) || (y % 2 == 1));  // true เพราะ x เป็นเลขคู่ (12 % 2 == 0) เป็น true

    // 9. (x > y) && (z < y)
    printf("9. (x > y) && (z < y) : %d\n", (x > y) && (z < y)); // false เพราะ (12 > 7) เป็น true แต่ (12 < 7) เป็น false

    return 0; 
}